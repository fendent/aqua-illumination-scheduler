export const colorMap = colorName => {
  switch (colorName) {
    case "deep_red":
      return "#800000";
    case "uv":
      return "#a174ff";
    case "violet":
      return "#5202ea";
    case "cool_white":
      return "#c6e2f5";
    case "green":
      return "#227e09";
    case "blue":
      return "#2e3ab9";
    case "royal":
      return "#171969";
  }
}
